# Crie um programa que leia dois números e traga o resultado deles
n1 = int(input('Digite um valor:'))
# Declarando int ou "Tipo primitivo"
n2 = int(input('Digite outro valor:'))
# Declarando a soma com a variável "s"
s = n1 + n2
# Exibe o valor no terminal
print('A soma entre {} e {} é igual a {}!'.format(n1, n2, s))
